# -------------------------------------------------------------
# 1) 기본적인 for 반복문
# -------------------------------------------------------------
# range(5)는 0부터 시작해서 5 직전(4)까지의 숫자를 자동으로 만들어준다.
# 즉, range(5) → [0, 1, 2, 3, 4] 와 같은 '숫자 나열 생성기'
# for i in range(5): → 이 숫자들을 하나씩 i 변수에 담아서 반복 실행한다.
for i in range(5):
    print(i)     # i가 0 → 1 → 2 → 3 → 4 순서로 바뀌며 출력됨



# -------------------------------------------------------------
# 2) 리스트(list)를 반복하는 for문
# -------------------------------------------------------------
# fruits는 문자열이 3개 들어 있는 '리스트'
fruits = ["apple", "banana", "cherry"]

# for f in fruits:
# 리스트 안의 요소를 왼쪽부터 하나씩 꺼내서 f라는 변수에 담아준다.
# f = "apple" → f = "banana" → f = "cherry" 순서로 반복됨
for f in fruits:
    print("과일:", f)



# -------------------------------------------------------------
# 3) 구구단 예제
# -------------------------------------------------------------
# range(1, 10)은 1부터 시작해서 10 직전(9)까지의 숫자를 만든다.
# 즉, [1, 2, 3, ..., 9]
for i in range(1, 10):
    # f"문자열 {변수}" : 파이썬의 문자열 포맷팅 문법
    # {i}와 {2 * i} 자리에는 실제 계산된 값이 들어간다.
    print(f"2 x {i} = {2 * i}")
    # 반복할 때마다 i 값이 1 → 2 → ... → 9로 변하면서 계산됨
