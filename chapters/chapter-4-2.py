# -------------------------------------------------------------
# 1) while 기본 구조
# -------------------------------------------------------------
# while 조건:
#     조건이 True(참)인 동안 계속 반복한다.
#
# 즉,
# while count <= 5:
# → count가 5 이하일 때만 반복하고,
#   6이 되면 조건이 False가 되어 반복을 멈춘다.

count = 1  # 반복을 시작할 초기 값

while count <= 5:       # count가 1~5일 동안 반복됨
    print(count)        # 현재 count 값 출력
    count += 1          # count = count + 1 과 동일
                         # 반복할 때마다 count가 1씩 증가
                         # 결국 6이 되면 조건(count <= 5)이 False가 되어 반복 종료

# -------------------------------------------------------------
# 2) 사용자 입력을 이용한 while 반복 종료
# -------------------------------------------------------------
# text 변수를 미리 빈 문자열("")로 만들어둔다.
# → exit이 아니기 때문에 기본적으로 while이 처음에는 한 번 실행될 수 있다.
