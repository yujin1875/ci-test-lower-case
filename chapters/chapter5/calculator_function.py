# ===============================
# 함수 정의 (아직 실행 안 됨)
# ===============================

def add(a, b):
    # a, b는 "매개변수(parameter)"
    # → 함수를 만들 때, 나중에 들어올 값을 담는 변수임

    result = a + b
    # 전달받은 a와 b를 더해서 result에 저장

    return result
    # return은 "결과를 돌려주고 함수 실행을 끝내라"는 의미


def sub(a, b):
    # 빼기 함수
    return a - b


# ===============================
# 함수 호출 (이때 실제로 실행됨)
# ===============================

# add(3, 5)를 호출
# → a = 3, b = 5 로 값이 들어감
x = add(3, 5)

# sub(10, 4)를 호출
# → a = 10, b = 4
y = sub(10, 4)

# 함수가 return 한 결과를 출력
print(x)  # 8
print(y)  # 6


####################################################
# parameter(매개변수) ≠ argument(전달값)
####################################################