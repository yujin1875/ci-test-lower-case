# [CHATPER-1. 변수]

# 파이썬에서는 JS와 달리 변수 타입을 따로 적지 않는다.
# 타입이 존재하지 않는 게 아니라, 
# 변수 타입을 따로 적지 않는 문법을 따른다.
# (자바스크립트처럼 let, const, number, string 같은 타입이나 키워드 없음)
# → 값이 무엇이냐에 따라 변수의 자료형(type)이 자동으로 결정된다.

name = "Alice"      # 문자열(str)
age = 20            # 정수(int)
height = 165.5      # 실수(float)
is_student = True   # 불리언(bool)

# 파이썬 변수는 '재할당'이 가능하다.
# 같은 변수명에 새로운 값을 넣으면 이전 값은 사라지고 새 값으로 바뀐다.
is_student = False  # 위 True는 덮어쓰기 되어 없어지고 False가 최종 값이 됨

print(name)
print(age)
print(height)
print(is_student)

############################################
# 변수 재할당 예시. 
# 위에서도 말했듯, 변수명이 같으면 새로운 값으로 덮어씌워진다.
# 이걸 변수 재할당이라고 부른다


# 10 출력, 여기서는 아래 x들과 달리 재할당하는게 아니라
# 최초로 x라는 변수에 값을 넣는거까
# 초기화 또는 할당이라고 부른다. 
# 지금 당장 외워야 하는 개념은 아니니 가볍게 참고만하고 넘어가자
x = 10
print(x)     

x = 99
print(x)     # 99 출력 (이전 값은 사라짐). 변수 재할당

x = "hello"
print(x)     # hello 출력 (정수였다가 갑자기 문자열로 바뀜). 변수 재할당

############################################
# 타입 자동 변경 예제 (초보들이 제일 많이 헷갈림)
# 같은 숫자라도 값이 정수(자연수)냐 실수냐에 따라 타입이 갈린다
num = 10
print(type(num))   # <class 'int'>

num = 10.5
print(type(num))   # <class 'float'>

# 따옴표로 감싸면 숫자일지라도 문자로 인식된다
num = "10"
print(type(num))   # <class 'str'>

num = True
print(type(num))   # <class 'bool'>

############################################
# 문자 + 숫자 혼동 조심할 것
# 붕어빵 더하기 33이 라는게 말이 안 되듯
# 문자에 숫자를 더하는 건 오류가 난다.

a = "5"    # 문자 5
b = 5      # 숫자 5

print(a + a)  # "55"  → 문자열이라 이어붙기
print(b + b)  # 10    → 숫자라 더하기

# 두 변수가 타입이 다르면, 정확히 둘다 숫자가 아니므로 연산 불가
# 주석을 풀고 한번 실행해보면, 에러가 나는 걸 확인할 수 있다.
# print(a + b)  

############################################
# 입력받는 값은 숫자를 받더라도 무조건 문자로 처리된다

age = input("나이 입력: ")  # 입력값은 무조건 문자열
print(age + age)            # 숫자가 아니므로, 문자를 이어붙인 결과가 나온다

age = int(age)              # 정수로 변환해야 숫자 연산 가능
print(age + age)
