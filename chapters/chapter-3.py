# 문자열(string)
# 파이썬에서 문자열은 사실 '문자의 묶음' → 즉, 문자들의 리스트와 비슷하게 취급된다.
# 그래서 문자열에도 인덱스(번호)를 사용하여 특정 글자를 뽑을 수 있다.
text = "Hello Python"

# .upper() : 문자열의 모든 소문자를 대문자로 바꾸는 함수
# 문자열을 수정하는 게 아니라, '대문자로 변환된 새로운 문자열'을 돌려준다.
print(text.upper())      # 결과: "HELLO PYTHON"
print(text.lower())      # 결과: ???

# text[0]
# 문자열에서 인덱스(index) 0번 글자를 의미.
# 인덱스는 0부터 시작한다. (파이썬, JS, C, Java 등 대부분 동일)
print(text[0])           # 결과: "H"



# -------------------------------------------------------------
# 리스트 (list)
# 여러 개의 데이터를 하나의 묶음으로 저장할 수 있는 자료형.
# JS의 배열(array)과 거의 동일한 개념이다.
fruits = ["apple", "banana", "cherry"]

# 인덱스를 이용해 특정 요소에 접근할 수 있다.
print(fruits[0])         # "apple"

# .append() : 리스트 맨 뒤에 새로운 데이터를 추가하는 기능
fruits.append("orange")

print(fruits)
# 결과: ["apple", "banana", "cherry", "orange"]
# 리스트는 순서가 있고, 수정/추가/삭제가 가능하다.



# -------------------------------------------------------------
# 딕셔너리(dict)
# 파이썬의 딕셔너리는 JS의 객체(object) 또는 JSON 구조와 거의 동일하다.
# key: value 형태로 데이터를 저장한다.
user = {
    "name": "Alice",
    "age": 20,
    "isAdmin": False
}

# user["name"]
# 딕셔너리에서 "name"이라는 key에 해당하는 값을 가져온다.
print(user["name"])   # "Alice"

# user.get("age")
# key가 존재하면 해당 값을 반환하고,
# key가 없으면 None(아무 값 없음)을 반환한다.
# ⇒ 안전하게 꺼낼 수 있는 방식
print(user.get("age"))  # 20

# user["없는키"] → 에러 발생(KeyError)
# user.get("없는키") → None 반환 (에러 없음)

# 따라서 두 문장은 “둘 다 값을 꺼낸다”라는 기능은 같지만,
# 없는 key를 사용할 때의 동작이 다르다.

# 정리:
# - user["name"] → 반드시 존재하는 key일 때 사용
# - user.get("name") → key가 없을 수도 있을 때 안전하게 사용
