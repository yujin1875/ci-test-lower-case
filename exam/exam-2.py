# -------------------------------------------------------------
# 버스 요금 계산 프로그램
# 요구사항:
# 1) 프로그램 시작 시 현재 가진 현금을 입력받는다.
# 2) 나이를 입력받고, 나이에 따라 요금을 다르게 계산한다.
# 3) 가진 돈이 요금보다 적으면 승차 불가 처리.
# 4) 충분한 돈이 있으면 요금을 지불하고 남은 잔액 출력.
# -------------------------------------------------------------

# 사용자에게 숫자 입력받기 (input은 문자열이므로 int로 변환해야 함)
money = int(input("현재 가진 현금 입력(원): "))
age = int(input("나이를 입력하세요: "))

# 개발자가 임의로 설정한 요금
YOUTH_FARE = 800     # 8~19세
ADULT_FARE = 1200    # 20~64세

# -------------------------------------------------------------
# 나이에 따라 요금 또는 메시지를 정하기
# -------------------------------------------------------------

if age <= 7:
    # 0~7세: 무료
    print("무료 승차 대상입니다.")
    print("남은 잔액:", money, "원")

elif age <= 19:
    # 8~19세: 청소년 요금
    fare = YOUTH_FARE
    print("청소년입니다. 요금은", fare, "원 입니다.")

    # 돈이 충분한지 확인
    if money < fare:
        print("잔액이 부족합니다. 승차 불가!")
    else:
        money -= fare          # 요금 지불
        print("승차 완료! 현재 남은 잔액:", money, "원")

elif age <= 64:
    # 20~64세: 성인 요금
    fare = ADULT_FARE
    print("성인입니다. 요금은", fare, "원 입니다.")

    # 돈이 충분한지 확인
    if money < fare:
        print("잔액이 부족합니다. 승차 불가!")
    else:
        money -= fare          # 요금 지불
        print("승차 완료! 현재 남은 잔액:", money, "원")

else:
    # 65세 이상: 무료
    print("경로 우대, 무료 승차 대상입니다.")
    print("남은 잔액:", money, "원")
