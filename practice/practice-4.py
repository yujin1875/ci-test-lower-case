# =============================================================
# [미니 계산기 과제]
# =============================================================
#
# 이 과제는 동일한 기능의 프로그램을
# 1) 함수지향 방식
# 2) 객체지향 방식
# 으로 각각 구현하는 과제이다.
#
# 목적은 "어떤 방식이 더 좋은지"를 판단하는 것이 아니라,
# 코드 구조와 실행 흐름의 차이를 경험하는 것이다.
#
# =============================================================
# 공통 기능 요구사항 (과제 1 / 과제 2 공통)
# =============================================================
#
# 1. 프로그램은 실행되면 메뉴를 출력해야 한다.
#
#    예)
#    1. 더하기
#    2. 빼기
#    3. 종료
#
# 2. 프로그램은 while문을 사용하여 계속 반복 실행되어야 한다.
#
# 3. 사용자는 메뉴 번호를 입력하여 동작을 선택한다.
#
# 4. 더하기 또는 빼기를 선택한 경우:
#    - 숫자 2개를 입력받는다.
#    - 입력받은 숫자를 이용해 계산을 수행한다.
#    - 계산 결과를 출력한다.
#    - 계산이 끝나면 다시 메뉴로 돌아간다.
#
# 5. 종료를 선택한 경우:
#    - 프로그램을 종료한다.
#
# 6. 메뉴에 없는 값을 입력한 경우:
#    - "잘못된 입력입니다." 를 출력한다.
#    - 프로그램은 종료되지 않고 다시 메뉴를 출력한다.
#
# -------------------------------------------------------------
# 입력 관련 주의사항 (공통)
# -------------------------------------------------------------
#
# - input()으로 입력받은 값은 문자열이므로,
#   계산을 위해 숫자형(int)으로 변환해야 한다.
#
# - 숫자가 아닌 값을 입력했을 경우의 예외 처리는
#   필수가 아니다. (선택 사항)
#
# =============================================================
# [과제 1] 함수지향 방식 계산기
# =============================================================
#
# 1. 계산 기능은 함수로 구현해야 한다.
#
# 2. 각 함수는 다음 조건을 만족해야 한다.
#    - 매개변수로 숫자 2개를 받는다.
#    - 계산 결과를 return으로 반환한다.
#
# 3. main 파일에서는:
#    - 메뉴 출력
#    - 사용자 입력 처리
#    - 함수 호출
#    만을 담당한다.
#
# 4. 계산 로직은 main 파일에 직접 작성하지 않는다.
#
# 5. 계산 함수는 별도의 파일(모듈)에 작성하고,
#    main 파일에서 import 하여 사용한다.
#
# =============================================================
# [과제 2] 객체지향 방식 계산기 (리팩토링)
# =============================================================
#
# 1. 과제 1에서 만든 기능을 객체지향 방식으로 리팩토링한다.
# 👉🏻기존의 파일을 지우거나 수정하지는 말고 새로운 py파일을 만들어서 작업하자.
#
# 2. Calculator 클래스를 작성해야 한다.
#
# 3. 계산 기능은 클래스의 메서드로 구현해야 한다.
#
# 4. 메서드는 다음 조건을 만족해야 한다.
#    - 매개변수로 숫자 2개를 받는다.
#    - 계산 결과를 return으로 반환한다.
#
# 5. main 파일에서는:
#    - Calculator 객체를 생성한다.
#    - 생성한 객체의 메서드를 호출하여 계산을 수행한다.
#
