# [파이썬 과제 – 버스 요금 계산 프로그램]
# -------------------------------------------------------------
# 요구사항:
#
# 1. 프로그램 시작 시 사용자에게 "현재 보유한 현금"을 입력받는다.
#
# 2. 사용자에게 "나이"를 입력받는다.
#
# 3. 나이에 따라 버스 요금을 다음 기준으로 계산한다.
#    - 0~7세     → "무료 승차"
#    - 8~19세    → "청소년 요금 000원"
#                  (요금 금액은 개발자가 직접 변수로 설정할 것)
#    - 20~64세   → "성인 요금 000원"
#                  (요금 금액은 개발자가 직접 변수로 설정할 것)
#    - 65세 이상 → "경로 우대, 무료 승차"
#
# 4. 요금이 필요한 나이 구간(8~64세)이라면,
#    보유 현금이 요금보다 적을 경우 → "승차 불가" 안내 메시지를 출력한다.
#       예시: "잔액이 부족합니다. 승차 불가!"
#
# 5. 요금이 필요한 나이 구간(8~64세)에서,
#    보유 현금이 충분할 경우 → 요금을 차감하고 잔액을 출력한다.
#       예시: "청소년입니다. 현재 남은 잔액: 000원"
#
# 6. 무료 승차 구간(0~7세, 65세 이상)은 요금 차감 없이
#    “무료 승차” 메시지와 함께 현재 잔액을 출력한다.
#
# 7. 조건문(if / elif / else)을 활용하여 전체 로직을 구현할 것.
#
# 8. 요금 금액은 학생이 직접 변수로 지정하여 사용할 것.
#
