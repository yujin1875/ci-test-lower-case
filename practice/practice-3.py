# [구구단 과제 1] - 하드코딩된 구구단 출력
# -------------------------------------------------------------
# 요구사항:
#
# 1. 특정 단(예: 2단 또는 3단)을 개발자가 직접 코드에 하드코딩하여 출력한다.
#    예: 2단이라면 2 x 1 ~ 2 x 9 까지 반복해서 출력.
#
# 2. 반복문(for)을 사용하여 1부터 9까지 곱셈 결과를 출력한다.
#
# 3. 이 과제에서는 사용자 입력을 받지 않는다.
#    (개발자가 코드 안에 단수를 직접 적어서 출력하는 형태)
#
