# -------------------------------------------------------------
# [구구단 과제 2] - 입력값에 따라 단과 범위를 출력하는 구구단
# -------------------------------------------------------------
# 요구사항:
#
# 1. 사용자에게 "몇 단을 출력할지" 입력받는다.
#    예: 입력값이 7이면 7단을 출력한다.
#
# 2. 사용자에게 "어디부터 어디까지 곱할지 범위"를 입력받는다.
#    예: 3~6을 입력하면 7 x 3 부터 7 x 6 까지 출력한다.
#
# 3. 입력받은 단과 범위에 따라 구구단을 유동적으로 출력해야 한다.
#
# 4. 출력이 끝난 뒤 프로그램은 다음 질문을 출력한다.
#      → "계속할까요? (yes/no)"
#
# 5. 사용자가 yes라고 입력하면 다시 새로운 단과 범위를 입력받아 반복한다.
#
# 6. 사용자가 no라고 입력하면 프로그램을 종료한다.
#
# 7. while문 또는 반복 구조를 이용하여 계속/종료 흐름을 제어할 것.
#
# -------------------------------------------------------------

# -------------------------------------------------------------
# [구구단 과제 3] - 사용자 입력 기반 "반복 실행" 구구단 (메뉴형)
# -------------------------------------------------------------
# 요구사항:
#
# 1. 프로그램은 while문을 이용해 계속 반복 실행된다.
#
# 2. 매 반복마다 사용자에게 다음 값을 입력받는다.
#     - 출력할 단
#     - 곱셈 범위(시작값 ~ 끝값)
#
# 3. 입력받은 값에 따라 구구단 결과를 출력한다.
#
# 4. 출력이 끝난 뒤 프로그램은 반드시 질문을 출력한다:
#       → "계속하시겠습니까? (yes/no)"
#
# 5. 사용자가 yes 입력 → while 반복 계속 진행
#
# 6. 사용자가 no 입력 → 프로그램 종료
#
# 7. 그 외 다른 입력 → "잘못된 입력입니다." 출력 후 다시 yes/no 질문
#
# -------------------------------------------------------------