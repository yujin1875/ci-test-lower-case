def reverse_string(s: str) -> str:
    """문자열을 뒤집어서 반환하는 함수"""
    return s[::-1]

# 파이썬 슬라이싱 문법
#
# s[start : stop : step]
#
# start : 어디서 시작할지 (기본값 = 처음)
# stop : 어디까지 갈지 (기본값 = 끝까지)
# step : 몇 칸씩 이동할지
# (기본값 = 1 → 왼→오 방향)
#
# 1) start 생략 → 처음부터 시작
# 2) stop 생략 → 끝까지
# 3) step = -1 → "거꾸로 한 칸씩" 이동
#
# 바로 이 -1 때문에 문자열이 뒤집힘