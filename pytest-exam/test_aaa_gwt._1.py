def get_discount_price(price, discount_percent):
    """할인된 가격을 계산하는 단순 함수"""
    # 할인액 = 원래 가격 * (할인율 ÷ 100)
    discount = price * discount_percent / 100
    # 최종 판매가 = 원래 가격 - 할인액
    return price - discount


# =======================================================================
# 1) AAA 패턴 (Arrange → Act → Assert)
# -----------------------------------------------------------------------
# - 개발자 사이에서 가장 널리 쓰는 단위 테스트 패턴
# - 테스트를 "준비 / 실행 / 검증" 으로 명확하게 나눈다.
# - 함수 테스트, 클래스 테스트, 계산 로직 검증 등에 적합함.
# - 작성자 중심: "내가 테스트를 이해하기 쉽도록" 단계별로 정리함.
# =======================================================================
def test_discount_aaa():
    """AAA 패턴으로 할인 테스트"""

    # Arrange (준비 단계)
    # - 테스트에 필요한 값 또는 객체를 세팅하는 과정
    price = 10000
    discount_percent = 20
    
    # Act (실행 단계)
    # - 테스트할 실제 기능을 호출함
    result = get_discount_price(price, discount_percent)
    
    # Assert (검증 단계)
    # - 실행 결과가 예상과 같은지 확인
    assert result == 8000



# =======================================================================
# 2) GWT(Given → When → Then) 패턴 
# -----------------------------------------------------------------------
# - 사람이 읽는 "시나리오 설명" 같은 구조
# - BDD(Behavior Driven Development, 행동 주도 개발)에서 대표적으로 사용됨
# - 테스트가 '요구사항 문서'처럼 읽힌다:
#       Given(어떤 상황이 주어지고)
#       When(어떤 행동을 하면)
#       Then(어떤 결과가 나와야 한다)
# - 기획자, QA, 비개발자도 쉽게 이해할 수 있는 테스트 설명 방식
# =======================================================================
def test_discount_gwt():
    """Given-When-Then 패턴으로 할인 테스트"""

    # Given: 5000원 상품과 10% 할인이 있을 때
    # - 상황 정의(시나리오의 배경)
    price = 5000
    discount_percent = 10
    
    # When: 할인 가격을 계산하면
    # - 사용자가 어떤 행동을 했을 때
    result = get_discount_price(price, discount_percent)
    
    # Then: 결과는 4500원이어야 한다
    # - 기대되는 결과
    assert result == 4500
